let a = 5;
let b = 2;

console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a % b =", a % b);
console.log("a ** b =", a ** b);

//Unary Operators 
console.log("a++ =", a++);
console.log(a);

console.log("--b =", --b);
console.log(b);